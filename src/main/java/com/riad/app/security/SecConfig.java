@Configuration
@EnableWebSecurity
@RequiredArgsConstructor
public class SecConfig  {
	private final PasswordEncoder passwordEncoder;
	@Bean
	UserDetailsService userDetailsService() {
		return new InMemoryUserDetailsManager(
				User.withUsername("ali").password(passwordEncoder.encode("1234")).authorities("GC").build(),
				User.withUsername("khalid").password(passwordEncoder.encode("1234")).authorities("C").build(),
				User.withUsername("amine").password(passwordEncoder.encode("1234")).authorities("GD").build()
				
				);
	}
	@Bean
	SecurityFilterChain filter(HttpSecurity http) throws Exception {
		return http.csrf(csrf->csrf.disable())
.httpBasic().and()				.authorizeHttpRequests(auth->auth.anyRequest().authenticated())
				.build();
	}
	
}
